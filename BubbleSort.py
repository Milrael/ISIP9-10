def bubbleSort(arr):
    swapped = False
    for i in range(len(arr) - 1, 0, -1):
        for j in range(i):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True
        if swapped:
            swapped = False
        else: 
            break
    return arr



